def save_movies_data():
    """–°–æ—Ö—Ä–∞–Ω—è–µ—Ç –¥–∞–Ω–Ω—ã–µ –æ —Ñ–∏–ª—å–º–∞—Ö –≤ JSON-—Ñ–∞–π–ª"""
    # –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ –¥–ª—è —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è (–±–µ–∑ URL, —Ç–æ–ª—å–∫–æ –ø—É—Ç–∏ –∫ –ª–æ–∫–∞–ª—å–Ω—ã–º —Ñ–∞–π–ª–∞–º)
    movies_data = []
    for movie in MOVIES:
        movie_data = {
            "id": movie["id"],
            "title": movie["title"],
            "original_title": movie["original_title"],
            "year": movie["year"],
            "rating": movie["rating"],
            "duration": movie["duration"],
            "genre": movie["genre"],
            "director": movie["director"],
            "description": movie["description"],
            "image": f"/images/movies/{movie['filename']}",
            "cast": movie["cast"]
        }
        movies_data.append(movie_data)
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ JSON-—Ñ–∞–π–ª
    data_file = DATA_DIR / "movies.json"
    with open(data_file, 'w', encoding='utf-8') as f:
        json.dump(movies_data, f, ensure_ascii=False, indent=2)
    
    print(f"‚úÖ –î–∞–Ω–Ω—ã–µ –æ —Ñ–∏–ª—å–º–∞—Ö —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã –≤ {data_file}")
    return True

